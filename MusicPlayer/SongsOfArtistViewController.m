- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    // Configure the cell...
    if(cell == nil)
    {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:CellIdentifier];
    }
    MPMediaItem *musicItem = [[self.songsOfArtist items] objectAtIndex:indexPath.row];
    cell.textLabel.font = [UIFont fontWithName:@"System" size:17];
    cell.textLabel.text = [musicItem valueForProperty:MPMediaItemPropertyTitle];
    cell.detailTextLabel.text = [NSString stringWithFormat:@"%@ - %@",[musicItem valueForProperty:MPMediaItemPropertyArtist],[musicItem valueForProperty:MPMediaItemPropertyAlbumTitle]];
    return cell;
}

